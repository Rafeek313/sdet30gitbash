package eclipseDemo;
import java.util.*;
public class MainSum {

	public static void main(String[] args) {
		Scanner sc=new Scanner(System.in);
		System.out.println("enter two integer value");
		int a=sc.nextInt();
		int b=sc.nextInt();
        System.out.println("a"+"+"+"b"+"="+(a+b));
        System.out.println("a"+"-"+"b"+"="+(a-b));
        System.out.println("a"+"*"+"b"+"="+a*b);
        System.out.println("a"+"/"+"b"+"="+a/b);
	}

}
